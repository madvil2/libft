#include "libft.h"

int	ft_word_len(char const *s, char c)
{
	int len;

	len = 0;
	while (*s == c)
		s++;
	while (*s != c && *s != '\0')
		while (*s != c && *s != '\0')
		{
			len++;
			s++;
		}
	return (len);
}
